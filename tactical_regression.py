def parse_engine_options(raw_options: list[str] | None) -> list[tuple[str, str]]:
    options: list[tuple[str, str]] = []
    for raw in raw_options or []:
        if "=" not in raw:
            raise ValueError(f"engine option must be Name=Value: {raw}")
        name, value = raw.split("=", 1)
        name = name.strip()
        value = value.strip()
        if not name or not value:
            raise ValueError(f"engine option must be Name=Value: {raw}")
        options.append((name, value))
    return options


def last_info_time_ms(output: str) -> int | None:
    matches = re.findall(r"\btime\s+(\d+)", output)
    if not matches:
        return None
    return int(matches[-1])


