def bestmove(binary: str, test: dict, options: list[tuple[str, str]]) -> tuple[str, str]:
    fen = test["fen"]
    if "movetime" in test:
        go = f"go movetime {test['movetime']}"
    elif "clock" in test:
        wtime, btime, winc, binc = test["clock"]
        go = f"go wtime {wtime} btime {btime} winc {winc} binc {binc}"
    else:
        go = f"go depth {test['depth']}"

    option_commands = "".join(
        f"setoption name {name} value {value}\n"
        for name, value in options
    )
    command = f"uci\nisready\n{option_commands}position fen {fen}\n{go}\nquit\n"
    output = subprocess.check_output(
        [binary],
        input=command,
        text=True,
        stderr=subprocess.STDOUT,
        timeout=test.get("timeout", 30),
    )
    match = re.search(r"bestmove\s+(\S+)", output)
    if not match:
        raise RuntimeError(f"Could not find bestmove in engine output:\n{output}")
    return match.group(1), output


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--binary", default="./mantis", help="Path to the Mantis binary")
    parser.add_argument(
        "--retries",
        type=int,
        default=0,
        help="Retry failed clock/movetime tests this many times before failing",
    )
    parser.add_argument(
        "--option",
        action="append",
        default=[],
        help="UCI option Name=Value applied before every test; repeatable",
    )
    parser.add_argument(
        "--match",
        action="append",
        default=[],
        help="Only run tests whose name contains this text; repeatable",
    )
    args = parser.parse_args()
    try:
        options = parse_engine_options(args.option)
    except ValueError as exc:
        parser.error(str(exc))
    if args.retries < 0:
        parser.error("--retries must be non-negative")

    tests = TESTS
    if args.match:
        matches = [needle.lower() for needle in args.match]
        tests = [
            test for test in TESTS
            if any(needle in test["name"].lower() for needle in matches)
        ]
        if not tests:
            parser.error("--match did not select any tests")

    for test in tests:
        attempts = 1 + args.retries if ("clock" in test or "movetime" in test) else 1
        last_failure: tuple[str, str] | None = None
        for attempt in range(1, attempts + 1):
            move, output = bestmove(args.binary, test, options)
            failure = ""
            if "expected" in test and move not in test["expected"]:
                expected = ", ".join(sorted(test["expected"]))
                failure = f"bestmove={move}, expected one of {{{expected}}}"
            elif move in test.get("banned", set()):
                failure = f"bestmove={move}"
            elif "max_info_time_ms" in test:
                info_time_ms = last_info_time_ms(output)
                if info_time_ms is None or info_time_ms > test["max_info_time_ms"]:
                    failure = f"info_time_ms={info_time_ms}, max={test['max_info_time_ms']}"

            if not failure:
                retry_note = f" after {attempt} attempts" if attempt > 1 else ""
                print(f"ok {test['name']}: bestmove={move}{retry_note}")
                last_failure = None
                break

            last_failure = (failure, output)
            if attempt < attempts:
                print(f"retry {test['name']}: {failure}")

        if last_failure is not None:
            failure, output = last_failure
            print(f"FAIL {test['name']}: {failure}")
            print(output)
            return 1

    print("All tactical regressions passed.")
    return 0


if __name__ == "__main__":
    sys.exit(main())
